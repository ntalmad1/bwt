    /**
     */
    private final String cssName;



    /**
     * Hidden Constructor
     *
     * @param className String
     */
    Glyphicon (String className)
    {
        this.cssName = className;
    }

    
    /**
     * 
     * @param icon Glyphicon
     * @return Html
     */
    public static Html create (Glyphicon icon)
    {
    	Html span = Html.span();
    	
    	span.ensureCssName("glyphicon");
    	span.ensureCssName(icon.getCssName());
    	
    	return span;
    }
    

    /**
     *
     * @return String
     */
    public String getCssName ()
    {
        return this.cssName;
    }
